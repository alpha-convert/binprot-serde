use crate::error::BPErr;

pub struct BPDeserializer<R : Read> {
    reader : R
}

impl<R : Read> BPDeserializer<R> {
    fn deserialize_nat0(&mut self) -> Result<u64,BPErr> {
        let mut header = 0 as u8;
        self.reader.read_exact(&mut [header]);
        Ok(9)
    }
}

